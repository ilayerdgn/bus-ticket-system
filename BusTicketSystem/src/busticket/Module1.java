    private void addConductorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addConductorActionPerformed
        try {
            Connection con = OracleConnection.getCon();
            PreparedStatement pst = con.prepareStatement("insert into Conductor (firstName, lastName, TC, contactNo, DOB, gender) values(?,?,?,?,?,?)");
            pst.setString(1, jTextField2.getText());
            pst.setString(2, jTextField2.getText());
            pst.setString(3, jTextField3.getText());
            pst.setString(4, jTextField4.getText());
            SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");
            String dateOfBirth = sdf.format(jDateChooser1.getDate());
            pst.setString(5, dateOfBirth);
            pst.setString(6, (String) jComboBox1.getSelectedItem());
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Şoför kaydı başarıyla oluşturuldu!");
            jTextField1.setText(null);
            jTextField2.setText(null);
            jTextField3.setText(null);
            jTextField4.setText(null);
            jDateChooser1.setDate(null);
            jComboBox1.setSelectedIndex(1);
        } catch (SQLException | HeadlessException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
        showConductorTable();
    }//GEN-LAST:event_addConductorActionPerformed

    private void deleteConductorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteConductorActionPerformed
        try {
            String sql = "DELETE FROM conductor WHERE conductorID=?";
            Connection con = OracleConnection.getCon();
            PreparedStatement pst = con.prepareStatement(sql);
            int getSelectedRowForDeletion = conductorTable.getSelectedRow();
            Object val = conductorTable.getValueAt(getSelectedRowForDeletion, 0);
            pst.setInt(1, (int) val);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Başarıyla silindi!");
            jTextField1.setText(null);
            jTextField2.setText(null);
            jTextField3.setText(null);
            jTextField4.setText(null);
            jDateChooser1.setDate(null);
            jComboBox1.setSelectedIndex(1);
        } catch (SQLException | HeadlessException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
        showConductorTable();
    }//GEN-LAST:event_deleteConductorActionPerformed

    private void updateConductorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateConductorActionPerformed
        try {
            Connection con = OracleConnection.getCon();
            PreparedStatement pst = con.prepareStatement("UPDATE conductor SET firstName=?, lastName=?, TC=?, contactNo=?, DOB=?, gender=? WHERE conductorID=?");
            int getSelectedRowForUpdate = conductorTable.getSelectedRow();
            Object val = conductorTable.getValueAt(getSelectedRowForUpdate, 0);
            pst.setInt(7, (int) val);

            pst.setString(1, jTextField2.getText());
            pst.setString(2, jTextField2.getText());
            pst.setString(3, jTextField3.getText());
            pst.setString(4, jTextField2.getText());
            SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");
            String dateOfBirth = sdf.format(jDateChooser1.getDate());
            pst.setString(5, dateOfBirth);
            pst.setString(6, jTextField4.getText());
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Başarıyla güncellendi!");
            jTextField1.setText(null);
            jTextField2.setText(null);
            jTextField3.setText(null);
            jTextField4.setText(null);
            jDateChooser1.setDate(null);
            jComboBox1.setSelectedIndex(0);
        } catch (SQLException | HeadlessException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
        showConductorTable();
    }//GEN-LAST:event_updateConductorActionPerformed

    private void conductorTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_conductorTableMouseClicked
        jTextField1.setText(conductorTable.getValueAt(conductorTable.getSelectedRow(), 1).toString());
        jTextField2.setText(conductorTable.getValueAt(conductorTable.getSelectedRow(), 2).toString());
        jTextField3.setText(conductorTable.getValueAt(conductorTable.getSelectedRow(), 3).toString());
        jTextField4.setText(conductorTable.getValueAt(conductorTable.getSelectedRow(), 4).toString());
        int getSelectedRowForUpdate = conductorTable.getSelectedRow();
        String val = (String) conductorTable.getValueAt(getSelectedRowForUpdate, 5);
        java.util.Date date = null;
        try {
            date = new SimpleDateFormat("dd-MM-yyyy").parse(val);
        } catch (ParseException ex) {
            Logger.getLogger(Module1.class.getName()).log(Level.SEVERE, null, ex);
        }
        jDateChooser1.setDate(date);
        jComboBox1.setSelectedItem(conductorTable.getValueAt(conductorTable.getSelectedRow(), 6).toString());
    }//GEN-LAST:event_conductorTableMouseClicked

