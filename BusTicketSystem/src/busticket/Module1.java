    public void showConductorTable() {
        try {
            Connection con = OracleConnection.getCon();
            String sql = "SELECT * FROM Conductor";
            PreparedStatement pst = con.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();

            DefaultTableModel route = (DefaultTableModel) conductorTable.getModel();
            route.setRowCount(0);

            while (rs.next()) {
                Object o[] = {rs.getInt("conductorID"), rs.getString("firstName"), rs.getString("lastName"), rs.getString("TC"), rs.getString("contactNo"), rs.getString("DOB"), rs.getString("gender")};
                route.addRow(o);
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }

