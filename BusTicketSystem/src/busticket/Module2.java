    private void deleteRouteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteRouteActionPerformed
        try {
            String sql = "DELETE FROM bus WHERE busID=?";
            Connection con = OracleConnection.getCon();
            PreparedStatement pst = con.prepareStatement(sql);
            int getSelectedRowForDeletion = busTable.getSelectedRow();
            Object val = busTable.getValueAt(getSelectedRowForDeletion, 0);
            pst.setInt(1, (int) val);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Otobüs kaydı başarıyla silindi!");
            jTextField1.setText(null);
            jTextArea1.setText(null);
        } catch (SQLException | HeadlessException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
        showRouteTable();
    }//GEN-LAST:event_deleteRouteActionPerformed

    private void updateBusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBusActionPerformed
        try {
            Connection con = OracleConnection.getCon();
            PreparedStatement pst = con.prepareStatement("UPDATE Bus SET busLicensePlate=?, details=? WHERE busID=?");
            int getSelectedRowForUpdate = busTable.getSelectedRow();
            Object val = busTable.getValueAt(getSelectedRowForUpdate, 0);
            pst.setInt(3, (int) val);
            pst.setString(1, jTextField1.getText());
            pst.setString(2, jTextArea1.getText());
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Başarıyla güncellendi!");
            jTextField1.setText(null);
            jTextArea1.setText(null);
        } catch (SQLException | HeadlessException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
        showRouteTable();
    }//GEN-LAST:event_updateBusActionPerformed

